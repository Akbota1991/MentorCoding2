package Practice_2;

public class SwapTwoNumbers {
    public static void main(String[] args) {


             int x = 10;
              int y = 5;




         x = x + y; // x now becomes 15   10+5=15

         y = x- y; // y now becomes 10   15-5=10

         x = x - y; // x becomes 5   15-10=5


        System.out.println("x = " + x);// 5
        System.out.println("y = " + y);// 10
    }
}
